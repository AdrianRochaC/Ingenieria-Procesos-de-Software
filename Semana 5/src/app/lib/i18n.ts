export type TranslationKey = keyof typeof translations.es;

export function getStoredLanguage(): Language {
  if (typeof window === "undefined") {
    return "es";
  }

  const stored = window.localStorage.getItem(STORAGE_KEY);
  return stored === "en" ? "en" : "es";
}

export function setStoredLanguage(language: Language) {
  if (typeof window === "undefined") {
    return;
  }

  window.localStorage.setItem(STORAGE_KEY, language);
}

export function tForLanguage(language: Language, key: TranslationKey, vars?: Record<string, string | number>) {
  let template = translations[language][key] ?? translations.es[key];
  if (!vars) return template;

  return Object.entries(vars).reduce(
    (result, [name, value]) => result.replaceAll(`{${name}}`, String(value)),
    template,
  );
}

export function translate(key: TranslationKey, vars?: Record<string, string | number>) {
  return tForLanguage(getStoredLanguage(), key, vars);
}
